export function searchWords(
  query: string,
  lang: "amharic" | "oromo" | "english" | "all" = "all"
): Word[] {
  const q = query.toLowerCase().trim();
  if (!q) return WORDS;
  return WORDS.filter((w) => {
    if (lang === "all" || lang === "amharic")
      if (w.amharic.includes(q) || w.romanization?.toLowerCase().includes(q))
        return true;
    if (lang === "all" || lang === "oromo")
      if (w.oromo.toLowerCase().includes(q)) return true;
    if (lang === "all" || lang === "english")
      if (w.english.toLowerCase().includes(q)) return true;
    return false;
  });
}

export function getWordById(id: string): Word | undefined {
  return WORDS.find((w) => w.id === id);
}

export const WORD_OF_THE_DAY = WORDS[Math.floor(Math.random() * WORDS.length)];
